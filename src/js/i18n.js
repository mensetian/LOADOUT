function getLang() {
  const stored = localStorage.getItem(LANG_KEY);
  return stored === 'en' || stored === 'es' ? stored : 'es';
}

function t(key, vars = {}) {
  const dict = I18N[getLang()] || I18N.es;
  let str = dict[key] ?? I18N.es[key] ?? key;
  for (const [k, v] of Object.entries(vars)) str = str.replaceAll(`{${k}}`, v);
  return str;
}

function dateLocale() { return getLang() === 'en' ? 'en-US' : 'es-CO'; }

function applyI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.dataset.i18nPlaceholder); });
  root.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.dataset.i18nTitle); });
  root.querySelectorAll('[data-i18n-aria]').forEach(el => { el.setAttribute('aria-label', t(el.dataset.i18nAria)); });
  document.documentElement.lang = getLang();
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  applyI18n();
  window.onLangChange?.();
}

function initLangButton() {
  const btn = document.querySelector('#langButton');
  if (!btn) return;
  const render = () => { btn.textContent = getLang() === 'en' ? 'EN' : 'ES'; };
  render();
  btn.onclick = () => { setLang(getLang() === 'en' ? 'es' : 'en'); render(); };
}

applyI18n();
initLangButton();
